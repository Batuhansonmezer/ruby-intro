# Put string

puts "Hello, world"

# In Ruby, there are different *types* of data:

# Numbers

puts 7.33

# Perform simple math with numbers

puts 45 + 78
# Strings
puts "tacos"
# puts "tacos" * 3 error

# Combine strings together
puts "#{3 + 9} tacos"

# Variables

quantity = 22 + 3 + 3
food = "tacos"

# Combine strings and variables
puts "#{quantity} #{food}" 

# String manipulation
puts "Hello".reverse
puts "Hello".upcase
puts "Hello".swapcase